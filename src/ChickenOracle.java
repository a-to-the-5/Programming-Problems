    public String theTruth(int n, int eggCount, int lieCount, int liarCount) {
        
        int x1 = (n-lieCount+liarCount-eggCount)/2;
        int x2 = (eggCount+liarCount-lieCount)/2;
        int egg = (n-lieCount+liarCount-2*x1);
        int chicken = (n-lieCount+liarCount-2*x2);
        if((int)x1==x1){
        	if((int)x2==x2)
        		return "Ambiguous";
        	else
        		return "The egg";
        } else {
        	if((int)x2==x2)
        		return "The chicken";
        	else
        		return "The oracle is a lie";
        }
    }

